package main

import "testing"

func TestSort(t *testing.T) {
	slice := []string{
		"31415926535897932384626433832795",
		"143543543534543534534534534534534423432423",
		"3434",
		"3123123213213212312411254232131231414111112312314141421314",
		"13",
		"5534534",
		"15435345345345345345",
		"3123123213213212312411254232131231414111112312314141421312",
	}

	actual := Sort(slice)
	expected := []string{
		"13",
		"3434",
		"5534534",
		"15435345345345345345",
		"31415926535897932384626433832795",
		"143543543534543534534534534534534423432423",
		"3123123213213212312411254232131231414111112312314141421312",
		"3123123213213212312411254232131231414111112312314141421314",
	}

	for i := 0; i < len(expected); i++ {
		if actual[i] != expected[i] {
			t.Errorf("Testing Failed at index %v!\nExpected: %v\nActual: %v", i, expected[i], actual[i])
		}
	}
}
